def _apple_wallet_configured() -> bool:
    return bool(
        settings.APPLE_PASS_TYPE_ID
        and settings.APPLE_PASS_TEAM_ID
        and settings.APPLE_PASS_CERT_PATH
        and settings.APPLE_PASS_KEY_PATH
        and settings.APPLE_WWDR_CERT_PATH
    )


def _download_avatar_bytes(video_id: str) -> bytes:
    """Download avatar PNG from GCS and return raw bytes."""
    from google.cloud import storage as gcs

    avatar_gcs_uri = f"gs://{settings.GCS_BUCKET}/avatars/{video_id}.png"
    client = gcs.Client(project=settings.GOOGLE_CLOUD_PROJECT)
    parts = avatar_gcs_uri.removeprefix("gs://").split("/", 1)
    bucket = client.bucket(parts[0])
    blob = bucket.blob(parts[1])
    return blob.download_as_bytes()


def _resize_image(img_bytes: bytes, size: tuple[int, int]) -> bytes:
    """Resize an image to the given (width, height) and return PNG bytes."""
    from PIL import Image

    img = Image.open(io.BytesIO(img_bytes)).convert("RGBA")
    img = img.resize(size, Image.LANCZOS)
    buf = io.BytesIO()
    img.save(buf, format="PNG")
    return buf.getvalue()


